/**
 * @brief 绘制单个字符
 */
void st7789_draw_char(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg, uint8_t size) {
    if (c < 0x20 || c > 0x7E) {  // 检查字符是否在有效范围内
        c = '?';  // 对于无效字符，绘制问号
    }
    
    // 计算字体字节索引
    c -= 0x20;  // 字体表从空格开始，所以减去0x20
    
    // 字体宽度是5，每个字符有5个字节数据
    for (uint8_t i = 0; i < 5; i++) {
        uint8_t line = font5x7[c * 5 + i];
        
        for (uint8_t j = 0; j < 8; j++) {
            if (line & 0x01) {  // 如果当前位是1
                if (size == 1) {
                    st7789_draw_pixel(x + i, y + j, color);
                } else {
                    st7789_fill_rect(x + i * size, y + j * size, size, size, color);
                }
            } else if (bg != color) {  // 如果当前位是0，且背景色与文字色不同
                if (size == 1) {
                    st7789_draw_pixel(x + i, y + j, bg);
                } else {
                    st7789_fill_rect(x + i * size, y + j * size, size, size, bg);
                }
            }
            line >>= 1;  // 移位到下一个位
        }
    }
}

/**
 * @brief 绘制字符串
 */
void st7789_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg, uint8_t size) {
    uint16_t cursor_x = x;
    uint16_t cursor_y = y;
    
    // 字符宽度(原始5像素 + 1像素间距)
    uint8_t char_width = 6 * size;
    
    while (*str) {
        st7789_draw_char(cursor_x, cursor_y, *str, color, bg, size);
        cursor_x += char_width;
        str++;
    }
} 